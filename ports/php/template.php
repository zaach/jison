			            $newError->state = $state;
			            $newError->symbol = $symbol;
			            $newError->lineNo = $this->yy->lineNo;
			            $newError->loc = $this->yy->loc;
			            throw $newError;
		            }

	                if (isset($r)) {
                        return $r;
                    }

                    // pop off stack
                    while ($len > 0) {
                        $len--;

                        array_pop($stack);
                        $stackCount--;

                        array_pop($vstack);
                        $vstackCount--;
                    }

                    if (is_null($_yy))
                    {
